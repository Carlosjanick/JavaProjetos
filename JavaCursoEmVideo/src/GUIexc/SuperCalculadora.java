    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        
        float nun = Float.parseFloat(tfvalor.getValue().toString());
        
        float restD = nun % 2;
        int elevC = (int) Math.pow(nun, 3);
        float raizQ = (float) Math.sqrt(nun);
        float raizCubica  = (float) Math.cbrt(nun);
        int valorAbs = (int) Math.abs(nun);
         
        lblDiv_res.setText(Float.toString(restD));
        lbl_cubica_res.setText(Float.toString(raizCubica));
        lbl_quadrada_res.setText(String.format("%.2f", raizQ));
        
        //lblabsoluto_res.setText(Integer.toString(valorAbs));
        lblabsoluto_res.setText(String.format("%d", valorAbs));
        
        lblcubo_res.setText(Integer.toString(elevC));
        
        
        lblQuadrada.setVisible(true);
        lbl_cubo.setVisible(true);
        lbl_div.setVisible(true);
        lblabsoluto.setVisible(true);
        lblcubica.setVisible(true);
        
        lblDiv_res.setVisible(true);
        lbl_cubica_res.setVisible(true);
        lbl_quadrada_res.setVisible(true);
        lblabsoluto_res.setVisible(true);
        lblcubo_res.setVisible(true);
        
    }//GEN-LAST:event_btnCalcularActionPerformed

